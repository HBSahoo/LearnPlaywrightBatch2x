//Pre increment

let a = 10;
let b = ++a;//increment a by 1 and then assign it to b;(a=a+1)
console.log(a);//11
console.log(b);//11

let d = 10;
let e = --d;//deincrement d by 1 and then assign it to e; (d= d-1)
console.log(d);//9
console.log(e);//9

//Pre increment -> ++a -> increase the value and assign the new value to the variable
